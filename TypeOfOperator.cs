using System;

public class TypeOfOperator
{
    public TypeOfOperator()
    {
        Console.WriteLine("Type of Integer is : " + typeof(int));
        Console.WriteLine("Type of Character is : " + typeof(char));
        Console.WriteLine("Type of short is : " + typeof(short));
        Console.WriteLine("Type of Long is : " + typeof(long));
        Console.WriteLine("Type of Double is : " + typeof(double));
        Console.WriteLine("Type of Byte is : " + typeof(byte));
        Console.WriteLine("Type of Float is : " + typeof(float));
        Console.WriteLine("Type of Boolean is : " + typeof(bool));
    }
}
